    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        Double price;
        BookRecord bookRecord;

        if (formMode == ModeEnum.ADD) {
            bookRecord = new BookRecord();
        } else {
            //update
            bookRecord = bookRecords.get(currentBookRecord);
        }
        
        try {
            bookRecord.setPrice(txt_price.getText());
        } catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Price format must be $$.$, where $ is a number.");
            txt_price.selectAll();
            txt_price.grabFocus();
            txt_price.requestFocus();
            return;
        }
        
        bookRecord.setCode(txt_code.getText());
        bookRecord.setTitle(txt_title.getText());
        
        db.updateRecord(bookRecord);
        bookRecords = db.FetchAll();
        setDefaultMode();
    }//GEN-LAST:event_updateButtonActionPerformed

