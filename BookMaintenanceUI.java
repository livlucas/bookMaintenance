    private void loadData() {
        db = new BookDatabase();
        db.initConnection();
        bookRecords = db.FetchAll();
    }
    
    private void displayCurrentBookRecord() {
        BookRecord bookRecord = bookRecords.get(currentBookRecord);
        
        setTitleLabel("Showing Record #" + bookRecord.getId());
        this.txt_code.setText(bookRecord.getCode());
        this.txt_title.setText(bookRecord.getTitle());
        this.txt_price.setText(bookRecord.getPrice().toString());
    }
    
    private void setDefaultMode() {
        if (bookRecords.isEmpty()) {
            setAddMode();
            return;
        }

        formMode = ModeEnum.DEFAULT;
        this.setEnabledAllButtons(true);
        updateButton.setEnabled(false);
        displayCurrentBookRecord();
    }
    //adding new records
    private void setAddMode() {
        formMode = ModeEnum.ADD;

        setTitleLabel("Adding new Record");
        txt_code.setText("");
        txt_price.setText("");
        txt_title.setText("");
        txt_code.setCaretPosition(0);
        
        setEnabledAllButtons(false);
        updateButton.setEnabled(true);
        updateButton.setText("Save");
    }
    //user in update mode
    private void setUpdateMode() {
        formMode = ModeEnum.UPDATE;
        
        setTitleLabel("Updating Record #" + bookRecords.get(currentBookRecord).getId());
        setEnabledAllButtons(false);
        updateButton.setEnabled(true);
    }
    
    //setting the title on top of program
    private void setTitleLabel(String message) {
        String count;
        if (bookRecords.isEmpty()) {
            count = "(empty db)";
        } else {
            count = "(total: " + bookRecords.size() + ")";
        }
        this.currentRecordLabel.setText(message + " " + count);
    }
    
