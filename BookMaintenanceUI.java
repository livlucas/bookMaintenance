    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        db.deleteRecord(bookRecords.get(currentBookRecord));
        bookRecords = db.FetchAll();
        
        if(currentBookRecord > bookRecords.size()-1){
            currentBookRecord = Math.max(bookRecords.size()-1, 0);
        }
        
        setDefaultMode();
    }//GEN-LAST:event_deleteButtonActionPerformed

